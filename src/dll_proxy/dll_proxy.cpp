bool LoadOriginalLibrary() {
    char sysPath[MAX_PATH] = { 0 };

    // Получаем путь к SysWOW64 (для 64-битной Windows)
    if (GetSystemWow64DirectoryA(sysPath, MAX_PATH) == 0) {
        // На 32-битной системе используем System32
        strcpy_s(sysPath, "C:\\Windows\\System32"); //Поменять, потому что может быть и не диск C
    }

    std::string fullPath = sysPath;
    fullPath += "\\dbghelp.dll";

    HMODULE hTemp = LoadLibraryA(fullPath.c_str());
    if (!hTemp)
        return false;

    hOriginalDll.reset(hTemp);

    // Загружаем адреса по ординалам
    for (size_t i = 0; i < 259; ++i) {
        g_OriginalFunctions[i] = GetProcAddress(hTemp, (LPCSTR)(g_Ordinals[i]));
        if (!g_OriginalFunctions[i]) {
            // Не критично, если какая-то функция не найдена, но для надёжности можно вернуть false
            // return false;
        }
    }
    return true;
}

void UnloadOriginalLibrary() {
    hOriginalDll.reset();
}

